    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        boolean bool=false;
        if (resTable.getSelectedRow()==-1){
            Toolkit.getDefaultToolkit().beep();
            if (um.getLanguageComboBox().getSelectedItem().equals("English") || um.getLanguageComboBox().getSelectedItem().equals("Choose a language"))
        JOptionPane.showMessageDialog(null, "You must select a user from the table!");
            else if (um.getLanguageComboBox().getSelectedItem().equals("עברית"))
                JOptionPane.showMessageDialog(null, "עליך לבחור משתמש מהרשימה!");
        }
        else
        {
            Object[] options = {"Yes","No"};
            Object[] hebOptions = {"כן","לא"};
            int n=0;
            if (um.getLanguageComboBox().getSelectedItem().equals("English") || um.getLanguageComboBox().getSelectedItem().equals("Choose a language"))
              n = JOptionPane.showOptionDialog(null, "Are you sure you want to proceed?","Removing confirmation",JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE,null,options,options[1]);
            else if (um.getLanguageComboBox().getSelectedItem().equals("עברית"))
              n = JOptionPane.showOptionDialog(null, "האם אתה בטוח שברצונך להמשיך?","אימות הסרה",JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE,null,hebOptions,hebOptions[1]);    
            if (n==0)
            {
                if (resTable.getValueAt(resTable.getSelectedRow(), 0).toString().equals("admin")){
                    Toolkit.getDefaultToolkit().beep();
            if (um.getLanguageComboBox().getSelectedItem().equals("English") || um.getLanguageComboBox().getSelectedItem().equals("Choose a language"))
                    JOptionPane.showMessageDialog(null, "You cannot remove the user: admin!");
            else if (um.getLanguageComboBox().getSelectedItem().equals("עברית"))
                JOptionPane.showMessageDialog(null, "אינך יכול להסיר את המשתמש: admin!");
                }
                else if (resTable.getValueAt(resTable.getSelectedRow(), 0).toString().equals(userName)){
                    Toolkit.getDefaultToolkit().beep();
            if (um.getLanguageComboBox().getSelectedItem().equals("English") || um.getLanguageComboBox().getSelectedItem().equals("Choose a language"))
                    JOptionPane.showMessageDialog(null, "You cannot remove the user you are currently logged in with!");
            else if (um.getLanguageComboBox().getSelectedItem().equals("עברית"))
                JOptionPane.showMessageDialog(null, "אינך יכול להסיר את המשתמש איתו אתה מחובר!");
                }
                else{
                bool=view.controller.removeUser( resTable.getValueAt(resTable.getSelectedRow(), 0).toString());
                if (bool==true)
                {
                    Toolkit.getDefaultToolkit().beep();
            if (um.getLanguageComboBox().getSelectedItem().equals("English") || um.getLanguageComboBox().getSelectedItem().equals("Choose a language"))
                    JOptionPane.showMessageDialog(null, "User removed successfully!");
            else if (um.getLanguageComboBox().getSelectedItem().equals("עברית"))
                    JOptionPane.showMessageDialog(null, "המשתמש הוסר בהצלחה!");
                    view.ShowDataUsers(resTable,um.getLanguageComboBox());
                }
                else{
                 Toolkit.getDefaultToolkit().beep();
            if (um.getLanguageComboBox().getSelectedItem().equals("English") || um.getLanguageComboBox().getSelectedItem().equals("Choose a language"))   
                JOptionPane.showMessageDialog(null, "User was not removed!");
            else if (um.getLanguageComboBox().getSelectedItem().equals("עברית"))
                JOptionPane.showMessageDialog(null, "המשתמש לא הוסר!");
                }
            }
        }
      }
    }//GEN-LAST:event_removeActionPerformed

